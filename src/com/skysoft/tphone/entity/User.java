package com.skysoft.tphone.entity;

public class User {
	
	private String u_uid;
	private String u_account;
	private String u_password;
	private String u_email;
	private String u_ip;
	private Integer u_state;
	private Integer u_num;
	public String getU_uid() {
		return u_uid;
	}
	public void setU_uid(String u_uid) {
		this.u_uid = u_uid;
	}
	public String getU_account() {
		return u_account;
	}
	public void setU_account(String u_account) {
		this.u_account = u_account;
	}
	public String getU_password() {
		return u_password;
	}
	public void setU_password(String u_password) {
		this.u_password = u_password;
	}
	public String getU_email() {
		return u_email;
	}
	public void setU_email(String u_email) {
		this.u_email = u_email;
	}
	public String getU_ip() {
		return u_ip;
	}
	public void setU_ip(String u_ip) {
		this.u_ip = u_ip;
	}
	public Integer getU_state() {
		return u_state;
	}
	public void setU_state(Integer u_state) {
		this.u_state = u_state;
	}
	public Integer getU_num() {
		return u_num;
	}
	public void setU_num(Integer u_num) {
		this.u_num = u_num;
	}
	
	

}
